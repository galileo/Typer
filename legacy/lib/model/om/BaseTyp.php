	 * of the class type constants TYPE_PHPNAME, TYPE_COLNAME, TYPE_FIELDNAME,
	 * TYPE_NUM. The default key type is the column's phpname (e.g. 'authorId')
	 *
	 * @param      array  $arr     An array to populate the object from.
	 * @param      string $keyType The type of keys the array uses.
	 * @return     void
	 */
	public function fromArray($arr, $keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = TypPeer::getFieldNames($keyType);

		if (array_key_exists($keys[0], $arr)) $this->setId($arr[$keys[0]]);
		if (array_key_exists($keys[1], $arr)) $this->setUserId($arr[$keys[1]]);
		if (array_key_exists($keys[2], $arr)) $this->setMeczId($arr[$keys[2]]);
		if (array_key_exists($keys[3], $arr)) $this->setGole1($arr[$keys[3]]);
		if (array_key_exists($keys[4], $arr)) $this->setGole2($arr[$keys[4]]);
		if (array_key_exists($keys[5], $arr)) $this->setStatus($arr[$keys[5]]);
		if (array_key_exists($keys[6], $arr)) $this->setPunkty($arr[$keys[6]]);
		if (array_key_exists($keys[7], $arr)) $this->setCreatedAt($arr[$keys[7]]);
		if (array_key_exists($keys[8], $arr)) $this->setUpdatedAt($arr[$keys[8]]);
	}

	/**
	 * Build a Criteria object containing the values of all modified columns in this object.
	 *
	 * @return     Criteria The Criteria object containing all modified values.
	 */
	public function buildCriteria()
	{
		$criteria = new Criteria(TypPeer::DATABASE_NAME);

		if ($this->isColumnModified(TypPeer::ID)) $criteria->add(TypPeer::ID, $this->id);
		if ($this->isColumnModified(TypPeer::USER_ID)) $criteria->add(TypPeer::USER_ID, $this->user_id);
		if ($this->isColumnModified(TypPeer::MECZ_ID)) $criteria->add(TypPeer::MECZ_ID, $this->mecz_id);
		if ($this->isColumnModified(TypPeer::GOLE1)) $criteria->add(TypPeer::GOLE1, $this->gole1);
		if ($this->isColumnModified(TypPeer::GOLE2)) $criteria->add(TypPeer::GOLE2, $this->gole2);
		if ($this->isColumnModified(TypPeer::STATUS)) $criteria->add(TypPeer::STATUS, $this->status);
		if ($this->isColumnModified(TypPeer::PUNKTY)) $criteria->add(TypPeer::PUNKTY, $this->punkty);
		if ($this->isColumnModified(TypPeer::CREATED_AT)) $criteria->add(TypPeer::CREATED_AT, $this->created_at);
		if ($this->isColumnModified(TypPeer::UPDATED_AT)) $criteria->add(TypPeer::UPDATED_AT, $this->updated_at);

		return $criteria;
	}

